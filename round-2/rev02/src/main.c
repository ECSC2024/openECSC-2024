void encrypt(uint32_t *xword, uint32_t *yword)
{
    LOAD_64(*xword, *yword, 0, 0);
    for (int step = 0; step < 10; step++) {
        ADD_KEY(step, 0, step, 0);
        ALZETTE(step, 0, 0, 0);
//#if FLAG == 0
        print(".");
//#endif
    }
    ADD_KEY(0, 0, 0, 0);
    STORE_64((uint32_t)xword, (uint32_t)yword, 0, 0);
}

